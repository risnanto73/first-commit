void main(List<String> args) {
  /**
   * simbol != menandakan not atau bukan
   * != dari ! = jadi !=
   */

  /**
   * tipe data boolean hany ada 2 nilai
   * true dan false
   */

  var isTrue = true;

  print(isTrue); // true
  print(!isTrue); // false
  print(true != false);
  print(false != false);
}
